// basic operations
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log(add(6, 7));
console.log(subtract(6, 7));
console.log(multiply(6, 7));
console.log(divide(6, 7));